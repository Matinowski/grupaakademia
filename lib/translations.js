  // Funkcja pomocnicza do tłumaczenia
  export function translate(key, fallback = key) {
    // Podziel klucz na sekcje (np. "header.Today" -> ["header", "Today"])
    const parts = key.split(".")
    
    if (parts.length === 1) {
      // Poszukaj we wszystkich sekcjach
      for (const section in translations) {
        if (translations[section][key]) {
          return translations[section][key]
        }
      }
      return fallback
    }
    
    // Jeśli klucz ma format section.key (np. "header.Today")
    const [section, subKey] = parts
    return translations[section] && translations[section][subKey]
      ? translations[section][subKey]
      : fallback
  }
  
  // Funkcja pomocnicza do formatowania dat
  export function formatDate(date, includeTime = false) {
    const options = { 
      year: 'numeric', 
      month: '2-digit', 
      day: '2-digit'
    }
    
    if (includeTime) {
      options.hour = '2-digit'
      options.minute = '2-digit'
    }
    
    return new Date(date).toLocaleDateString('pl-PL', options)
  }
  
  // Funkcja do tłumaczenia nazw miesięcy
  export function translateMonth(monthName) {
    return translations.months[monthName] || monthName
  }
  
  // Funkcja do tłumaczenia nazw dni tygodnia
  export function translateDay(dayName) {
    return translations.days[dayName] || dayName
  }
  
  // Funkcja do tłumaczenia skróconych nazw dni tygodnia
  export function translateDayShort(dayShort) {
    return translations.daysShort[dayShort] || dayShort
  }
  
  